int factorial(int n)
{
    if (n == 1 || n == 0)
        return 1;
    return (n * factorial(n - 1));
}

int main()
{
    int n;
    printf("Enter num less then 16 : ");
    scanf("%d",&n);
    printf("factoriall of %d is : %d ",n,factorial(n));
}
